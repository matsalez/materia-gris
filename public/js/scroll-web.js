$(document).ready(function() {
    $("#espacios").click(function () {
        $('html,body').animate({
            scrollTop: 1520
        }, 500);
    });

    $("#servicios").click(function () {
        $('html,body').animate({
            scrollTop: 1920
        }, 500);
    });

    $("#contacto").click(function () {
        $('html,body').animate({
            scrollTop: 710
        }, 500);
    });

});
